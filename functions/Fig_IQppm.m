function Fig_IQppm(X1, YMatrix1)
figure('InvertHardcopy','off','Color',[1 1 1],...
    'OuterPosition',[3053 151 576 514]);
axes1 = axes;
hold(axes1,'on');
semilogx1 = semilogx(X1,YMatrix1);
set(semilogx1(1),'DisplayName','Real.IP','LineWidth',3,'Color',[0 1 1]);
set(semilogx1(2),'DisplayName','Real.QP','LineWidth',3,...
    'Color',[0.996078431372549 0.647058823529412 0.658823529411765]);
set(semilogx1(3),'DisplayName','Inv.IP','LineWidth',1,'LineStyle','--',...
    'Color',[0 0 1]);
set(semilogx1(4),'DisplayName','Inv.QP','LineWidth',1,'LineStyle','--',...
    'Color',[1 0 0]);
ylabel('ppm','FontName','Times New Roman');
xlabel('Frequency (Hz)','FontName','Times New Roman');
title('Worst output compared with theory');
box(axes1,'on');
set(axes1,'FontName','Times New Roman','FontSize',12,'XGrid','on',...
    'XMinorGrid','on','XMinorTick','on','XScale','log','YGrid','on',...
    'YMinorGrid','on','YMinorTick','on');
legend1 = legend(axes1,'show');
set(legend1,...
    'Position',[0.141666669274369 0.137090779307898 0.178571425963725 0.223834319225217],...
    'FontSize',10.8);

